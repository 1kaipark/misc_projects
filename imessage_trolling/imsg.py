def randomcap(s):
    return ''.join([c.upper() if random.choice([True, False]) else c for c in s])

def caser(s: str) -> str:
    return ' '.join(
        [w.capitalize() for w in s.split(' ')]
    )

def stacker(s: str, *args) -> str:
    for arg in args:
        s = arg(s)
    return s